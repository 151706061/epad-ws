import java.util.Date;

import edu.stanford.epad.epadws.models.dao.AbstractDAO;

public class Subject extends AbstractDAO {

	long id;
	String subjectUID;
	String name;
	Date dob;
	String gender;
	String creator;
	Date createdTime;
	Date updateTime;

	public Subject(long id)
	{
		this.id = id;
	}
	
	public Subject() {
	}

	@Override
	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public String getSubjectUID() {
		return subjectUID;
	}

	public void setSubjectUID(String subjectUID) {
		this.subjectUID = subjectUID;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Date getDob() {
		return dob;
	}

	public void setDob(Date dob) {
		this.dob = dob;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getCreator() {
		return creator;
	}

	public void setCreator(String creator) {
		this.creator = creator;
	}

	public Date getCreatedTime() {
		return createdTime;
	}

	public void setCreatedTime(Date createdTime) {
		this.createdTime = createdTime;
	}

	public Date getUpdateTime() {
		return updateTime;
	}

	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}

	public final static String DBTABLE = "subject";
	public final static String[][] DBCOLUMNS = {
        {"id","long","id","Id"},
        {"subjectUID","String","subjectUID","varchar"},
        {"name","String","name","varchar"},
        {"gender","String","gender","varchar"},
        {"dob","Date","dob","date"},
        {"creator","String","creator","varchar"},
        {"createdTime","Date","createdtime","timestamp"},
        {"updateTime","Date","updatetime","timestamp"},	
	};

	@Override
	public String returnDBTABLE() {
		return DBTABLE;
	}

	@Override
	public String[][] returnDBCOLUMNS() {
		return DBCOLUMNS;
	}

}
